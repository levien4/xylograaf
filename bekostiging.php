            <h2>Zorgverzekering</h2>
            Vanaf 1 januari 2015 is de vergoede dyslexiezorg van zorgverzekeraar overgeheveld naar de gemeenten. Mijn werkzaamheden binnen de vergoede dyslexiezorg vinden in de NOP plaats vanuit mijn samenwerking met het Dyslexiecollectief en binnen de regio IJsselland in samenwerking met een regiebehandelaar. Het Dyslexiecollectief heeft contracten met alle gemeenten uit mijn werkgebied: de zorgregio Almere. Ook binnen de zorgregio IJsselland biedt de praktijk mogelijkheden voor vergoede zorg. De zorgregio IJsselland wordt gevormd door de gemeenten Dalfsen, Deventer, Hardenberg, Kampen, Olst-Wijhe, Ommen, Raalte, Staphorst, Steenwijkerland, Zwartewaterland en Zwolle.<p>

            Een voorwaarde voor vergoede hulp in deze regio is een toewijzing of indicatie door een goedgekeurde verwijzer, zoals de huisarts, jeugdarts of medisch specialist of de lokale Toegang.<p>

            <h2>Aanbod vanuit de vergoede zorg</h2>
            Het Dyslexiecollectief hanteert voor de vergoede dyslexiezorg de richtlijnen van het Protocol Dyslexie Diagnose en Behandeling (PDDB). De aanmelding voor de vergoede dyslexiezorg start bij een schooldossier waarin de hardnekkigheid en de door school geboden hulp beschreven staat. Als het dossier voldoet kan onderzoek plaatsvinden.<p>

